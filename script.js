function createTable(data) {
    // Determine the maximum length of any cell in the table
    let maxCellLength = 0;
    for (let i = 0; i < data.length; i++) {
        for (let j = 0; j < data[i].length; j++) {
            if (data[i][j].length > maxCellLength) {
                maxCellLength = data[i][j].length;
            }
        }
    }

    // Create the top border of the table
    let topBorder = "‚îå" + "‚îÄ".repeat(maxCellLength + 2) + "‚î¨" + "‚îÄ".repeat(maxCellLength + 2) + "‚î¨" + "‚îÄ".repeat(maxCellLength + 2) + "‚î¨" + "‚îÄ".repeat(maxCellLength + 2) + "‚îê";

    // Create the data rows of the table
    let dataRows = "";
    for (let i = 0; i < data.length; i++) {
        for (let j = 0; j < data[i].length; j++) {
            let cell = data[i][j];
            let padding = " ".repeat(Math.floor((maxCellLength - cell.length) / 2));
            let extraPadding = (cell.length % 2 == maxCellLength % 2) ? "" : " ";
            let formattedCell = "‚îÇ " + padding + cell + padding + extraPadding + " ";
            dataRows += formattedCell;
        }
        dataRows += "‚îÇ\n";
        if (i != data.length - 1) {
            dataRows += "‚îú" + "‚îÄ".repeat(maxCellLength + 2) + "‚îº" + "‚îÄ".repeat(maxCellLength + 2) + "‚îº" + "‚îÄ".repeat(maxCellLength + 2) + "‚îº" + "‚îÄ".repeat(maxCellLength + 2) + "‚î§\n";
        }
    }

    // Create the bottom border of the table
    let bottomBorder = "‚îî" + "‚îÄ".repeat(maxCellLength + 2) + "‚î¥" + "‚îÄ".repeat(maxCellLength + 2) + "‚î¥" + "‚îÄ".repeat(maxCellLength + 2) + "‚î¥" + "‚îÄ".repeat(maxCellLength + 2) + "‚îò";

    // Concatenate all the parts to create the final table
    let table = topBorder + "\n" + dataRows + bottomBorder;

  return { maxCellLength, table };
  
}

